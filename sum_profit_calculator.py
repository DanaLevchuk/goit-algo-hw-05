import re
from typing import Callable, Generator

def generator_numbers(text: str) -> Generator[float, None, None]:
    """
    –ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä, —â–æ –ø–æ–≤–µ—Ä—Ç–∞—î –≤—Å—ñ –¥—ñ–π—Å–Ω—ñ —á–∏—Å–ª–∞ –∑ —Ç–µ–∫—Å—Ç—É.
    –®—É–∫–∞—î —á–∏—Å–ª–∞ —É —Ñ–æ—Ä–º–∞—Ç—ñ 123.45, —è–∫—ñ –≤—ñ–¥–æ–∫—Ä–µ–º–ª–µ–Ω—ñ –ø—Ä–æ–±—ñ–ª–∞–º–∏.
    """
    pattern = r"(?<=\s)\d+\.\d+(?=\s)"
    for match in re.finditer(pattern, text):
        yield float(match.group())

def sum_profit(text: str, func: Callable[[str], Generator[float, None, None]]) -> float:
    """
    –ü—ñ–¥—Ä–∞—Ö–æ–≤—É—î —Å—É–º—É –≤—Å—ñ—Ö –¥—ñ–π—Å–Ω–∏—Ö —á–∏—Å–µ–ª, –∑–Ω–∞–π–¥–µ–Ω–∏—Ö —É —Ç–µ–∫—Å—Ç—ñ –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–æ–º func.
    """
    return sum(func(text))


