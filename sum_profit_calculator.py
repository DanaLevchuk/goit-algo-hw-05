        "1000.01 —è–∫ –æ—Å–Ω–æ–≤–Ω–∏–π –¥–æ—Ö—ñ–¥, –¥–æ–ø–æ–≤–Ω–µ–Ω–∏–π –¥–æ–¥–∞—Ç–∫–æ–≤–∏–º–∏ –Ω–∞–¥—Ö–æ–¥–∂–µ–Ω–Ω—è–º–∏ "
